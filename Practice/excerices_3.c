//Write a program to print average of 3 number
#include<stdio.h>
 void main()
 {
    int a,b,c;
    
    
    printf("enter the value of a");
    scanf("%d",&a);

    printf("enter the value of b");
    scanf("%d",&b);

    printf("enter the value of c");
    scanf("%d",&c);
     
    printf("average : %d",(a+b+c)/3);
 }